####Relational operator
x=10
y=20

# ==(exact equals to )
print(x==y)

# !=
print(x!=y)

# >

print(x>y)

#>= (Greater than or equals to)
print(x>=y)

#< 
print(x<y)

#<= (Less than or equals to)
print(x<=y)



a = 15
b = 22

# Find the maximum without branching statements
largest = (a * (a > b)) + (b * (b >= a))

print("The largest number is:", largest)
